# Programar em python, um programa que lê 5 números inteiros e calcula a média aritmética deles.

print("Digite o 1º número inteiro: ")
varNumero1 = int(input())
print("Digite o 2º número inteiro: ")
varNumero2 = int(input())
print("Digite o 3º número inteiro: ")
varNumero3 = int(input())
print("Digite o 4º número inteiro: ")
varNumero4 = int(input())
print("Digite o 5º número inteiro: ")
varNumero5 = int(input())

varMedia = ((varNumero1 + varNumero2 + varNumero3 + varNumero4 + varNumero5) / 5)

print("A média aritmética dos 5 números inteiros é: ", varMedia)